#!/usr/bin/env node
// generate-journey-script.js — Deterministic Playwright script generator.
// Reads component-map.json + extract-state.json and emits .mjs files.
// ~80% of code maps mechanically to interaction patterns; the remaining ~20%
// is marked with // LLM_FILL: comments for the LLM classifier to complete.
//
// Usage:
//   node generate-journey-script.js .artifacts/<KEY>/ --mode=verify [--screenshots=full]
//   node generate-journey-script.js .artifacts/<KEY>/ --mode=discover [--screenshots=key-only]

const { readFileSync, writeFileSync, existsSync } = require('fs');
const { join } = require('path');
const { createHash } = require('crypto');

// ── Args ──────────────────────────────────────────────────────────────
const artifactsDir = process.argv[2];
const args = process.argv.slice(3);
const mode = (args.find(a => a.startsWith('--mode=')) || '--mode=verify').split('=')[1];
const screenshots = (args.find(a => a.startsWith('--screenshots=')) || '--screenshots=full').split('=')[1];

if (!artifactsDir || !['verify', 'discover'].includes(mode)) {
  console.error('Usage: node generate-journey-script.js .artifacts/<KEY>/ --mode=verify|discover');
  process.exit(1);
}

// ── Inputs ────────────────────────────────────────────────────────────
const extractState = JSON.parse(readFileSync(join(artifactsDir, 'extract-state.json'), 'utf8'));
const componentMapPath = join(artifactsDir, 'component-map.json');
const componentMap = existsSync(componentMapPath)
  ? JSON.parse(readFileSync(componentMapPath, 'utf8'))
  : null;

// ── Cache hash ────────────────────────────────────────────────────────
// Content-based: if inputs haven't changed, the generated script is identical.
const hashInput = JSON.stringify({
  journeys: extractState.journey_definitions,
  personas: extractState.persona_selection,
  componentMap,
  mode,
  screenshots
});
const cacheHash = createHash('md5').update(hashInput).digest('hex').slice(0, 12);

const outputFile = mode === 'verify'
  ? join(artifactsDir, 'journey-test.mjs')
  : join(artifactsDir, 'persona-walkthrough.mjs');

// ── Cache check ───────────────────────────────────────────────────────
if (existsSync(outputFile)) {
  const existing = readFileSync(outputFile, 'utf8');
  const match = existing.match(/^\/\/ CACHE_HASH: (\w+)/);
  if (match && match[1] === cacheHash) {
    console.log(`Cache hit (${cacheHash}) — skipping regeneration of ${outputFile}`);
    process.exit(0);
  }
}

// ── Shared code blocks ───────────────────────────────────────────────
const targetPage = componentMap?.target_page || '';
const baseUrlVar = 'BASE_URL';

function sharedHeader() {
  return `// CACHE_HASH: ${cacheHash}
// Generated by generate-journey-script.js — do not edit mechanical sections.
// LLM_FILL blocks require agent completion before running.
import { chromium } from 'playwright';
import { readFileSync, writeFileSync, mkdirSync } from 'fs';

const ${baseUrlVar} = process.env.PROTOTYPE_URL || 'http://localhost:9000';
const ARTIFACTS = '${artifactsDir}';
const SCREENSHOTS = \`\${ARTIFACTS}/screenshots\`;
${componentMap ? `const componentMap = JSON.parse(readFileSync(\`\${ARTIFACTS}/component-map.json\`, 'utf8'));` : '// ponytail: no component-map.json found, selectors must come from LLM_FILL blocks'}

mkdirSync(SCREENSHOTS, { recursive: true });
`;
}

function pf6Utilities() {
  return `
// --- PF6 UTILITIES (tested, do not modify) ---

async function navigateTo(page, route) {
  await page.goto(\`\${${baseUrlVar}}\${route}\`);
  await page.waitForLoadState('domcontentloaded');
  await page.waitForTimeout(2000);
  await page.waitForSelector('tbody tr', { timeout: 8000 }).catch(() => null);
}

async function expandRow(page, rowText) {
  const row = page.locator('tbody tr').filter({ hasText: rowText }).first();
  const toggle = row.locator('td:first-child button').first();
  if (await toggle.isVisible({ timeout: 2000 }).catch(() => false)) {
    await toggle.click();
    await page.waitForTimeout(800);
  }
}

async function hoverElement(page, selector) {
  const el = page.locator(selector).first();
  if (await el.isVisible({ timeout: 2000 }).catch(() => false)) {
    await el.hover();
    await page.waitForTimeout(600);
  }
}

async function getTooltipText(page) {
  const tooltip = page.locator('.pf-v6-c-tooltip__content').first();
  if (await tooltip.isVisible({ timeout: 2000 }).catch(() => false)) {
    return await tooltip.textContent();
  }
  return null;
}

async function getColumnHeaders(page) {
  return await page.locator('thead th').allTextContents();
}

async function checkNoErrors(page) {
  const errors = await page.locator('.pf-v6-c-alert--danger, [role="alert"]').count();
  const bodyText = await page.locator('body').textContent().catch(() => '');
  return errors === 0 && !bodyText.includes('403') && !bodyText.includes('Forbidden');
}

async function screenshot(page, name) {
  await page.screenshot({ path: \`\${SCREENSHOTS}/\${name}\`, fullPage: false });
}
`;
}

function addInitScriptBlock() {
  return `
  // Pre-seed project selection and feature flags BEFORE page.goto()
  await page.addInitScript(() => {
    try { localStorage.setItem('selectedProject', JSON.stringify('All projects')); } catch {}
    try {
      const flags = JSON.parse(localStorage.getItem('featureFlags') || '{}');
      flags._lastModified = new Date().toISOString();
      localStorage.setItem('featureFlags', JSON.stringify(flags));
    } catch {}
  });`;
}

// ── Interaction pattern classifier ────────────────────────────────────
// Maps AC text + component-map data to mechanical interaction code.

function classifyInteraction(acId, acText) {
  const text = acText.toLowerCase();
  const mapping = componentMap?.ac_column_mapping?.[acId];

  if (mapping?.interaction === 'hover' || text.includes('tooltip') || text.includes('hover')) {
    const selector = mapping?.target
      ? `'${mapping.target}'`
      : `/* LLM_FILL: selector for ${acId} hover target */`;
    return { pattern: 'hover+assert', selector, type: 'tooltip' };
  }
  if (text.includes('expand') || text.includes('detail') || text.includes('resource')) {
    if (componentMap?.interactive_elements?.expandable_rows?.length) {
      return { pattern: 'expand+assert', type: 'expandable_row' };
    }
  }
  if (text.includes('column') || text.includes('table') || text.includes('display')) {
    return { pattern: 'navigate+verify', type: 'column_check' };
  }
  if (text.includes('disabled') || text.includes('hidden') || text.includes('no ') || text.includes('without')) {
    return { pattern: 'absence+verify', type: 'feature_absence' };
  }
  // Fallback: LLM decides
  return { pattern: 'LLM_FILL', type: 'unknown' };
}

// ── Verify mode: generate journey-test.mjs ───────────────────────────

function generateVerifyScript() {
  const journeys = extractState.journey_definitions || [];
  let journeyFunctions = '';

  for (const journey of journeys) {
    const acIds = journey.ac_ids || [];
    const acTexts = acIds.map(id => {
      const ac = (extractState.ac_list || []).find(a => a.criterion_id === id);
      return ac ? ac.text : '';
    });

    // Classify each AC's interaction pattern
    const interactions = acIds.map((id, i) => classifyInteraction(id, acTexts[i]));

    journeyFunctions += `
async function ${journey.id.replace(/-/g, '_')}(page) {
  const steps = [];
  const acIds = ${JSON.stringify(acIds)};

  await navigateTo(page, '${targetPage}');
  await screenshot(page, '${journey.id}-nav.png');
  steps.push({ step: 1, action: 'navigate', target: '${targetPage}', result: 'success', screenshot: 'screenshots/${journey.id}-nav.png', narration: 'Navigated to target page' });

`;

    // Generate mechanical interaction code based on classified pattern
    for (let i = 0; i < acIds.length; i++) {
      const interaction = interactions[i];
      const stepNum = i + 2;

      if (interaction.pattern === 'hover+assert') {
        journeyFunctions += `  // ${acIds[i]}: hover+assert (mechanical)
  await hoverElement(page, ${interaction.selector});
  const tooltipText${i} = await getTooltipText(page);
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'hover', target: '${acIds[i]} tooltip element', result: tooltipText${i} ? 'success' : 'fail', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: \`Hovered: tooltip \${tooltipText${i} ? 'visible' : 'not found'}\` });

`;
      } else if (interaction.pattern === 'expand+assert') {
        journeyFunctions += `  // ${acIds[i]}: expand+assert (mechanical)
  // LLM_FILL: identify which row text to expand for ${acIds[i]}
  // await expandRow(page, '<row text>');
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'expand', target: '${acIds[i]} expandable row', result: 'success', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: 'Expanded row to view details' });

`;
      } else if (interaction.pattern === 'navigate+verify') {
        const colMapping = componentMap?.ac_column_mapping?.[acIds[i]];
        if (colMapping?.column) {
          journeyFunctions += `  // ${acIds[i]}: navigate+verify column "${colMapping.column}" at index ${colMapping.index} (mechanical)
  const headers${i} = await getColumnHeaders(page);
  const colFound${i} = headers${i}.some(h => h.includes('${colMapping.column}'));
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'verify', target: '${colMapping.column} column', result: colFound${i} ? 'success' : 'fail', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: \`Column "${colMapping.column}" \${colFound${i} ? 'found' : 'missing'}\` });

`;
        } else {
          journeyFunctions += `  // ${acIds[i]}: navigate+verify (LLM_FILL: specify what to verify)
  // LLM_FILL: add verification logic for ${acIds[i]}: "${acTexts[i]}"
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'verify', target: '${acIds[i]}', result: 'success', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: '' });

`;
        }
      } else if (interaction.pattern === 'absence+verify') {
        journeyFunctions += `  // ${acIds[i]}: absence+verify — check feature is NOT present (mechanical)
  const noErrors${i} = await checkNoErrors(page);
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'verify-absence', target: '${acIds[i]}', result: noErrors${i} ? 'success' : 'fail', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: 'Verified absence of error indicators' });

`;
      } else {
        // LLM_FILL fallback
        journeyFunctions += `  // ${acIds[i]}: LLM_FILL — classify interaction and write verification
  // AC text: "${acTexts[i]}"
  // LLM_FILL: determine interaction type, write Playwright code, set result
  await screenshot(page, '${journey.id}-step-${stepNum}.png');
  steps.push({ step: ${stepNum}, action: 'verify', target: '${acIds[i]}', result: 'success', screenshot: 'screenshots/${journey.id}-step-${stepNum}.png', narration: '' });

`;
      }
    }

    journeyFunctions += `  const allPassed = steps.every(s => s.result === 'success');
  return { id: '${journey.id}', title: ${JSON.stringify(journey.title)}, ac_ids: acIds, verdict: allPassed ? 'PASS' : 'FAIL', steps_expected: ${acIds.length + 1}, steps_completed: steps.length, steps };
}
`;
  }

  // Main function
  const main = `
async function main() {
  const browser = await chromium.launch({ headless: true });
  const context = await browser.newContext({ viewport: { width: 1920, height: 900 } });
  const page = await context.newPage();
${addInitScriptBlock()}

  const results = [];
${journeys.map(j => `  results.push(await ${j.id.replace(/-/g, '_')}(page));`).join('\n')}

  await browser.close();
  writeFileSync(\`\${ARTIFACTS}/journey-results.json\`, JSON.stringify(results, null, 2));
  console.log(\`Completed \${results.length} journeys: \${results.filter(r => r.verdict === 'PASS').length} PASS, \${results.filter(r => r.verdict === 'FAIL').length} FAIL\`);
}

main().catch(console.error);
`;

  return sharedHeader() + pf6Utilities() + journeyFunctions + main;
}

// ── Discover mode: generate persona-walkthrough.mjs ──────────────────

function generateDiscoverScript() {
  const tasks = extractState.tasks_to_be_done || [];
  const personas = extractState.persona_selection?.selected || [];

  // Build task route mapping comment block
  let routeMapping = '// TASK ROUTES (from extract-state.json + component-map.json):\n';
  for (let i = 0; i < tasks.length; i++) {
    const task = tasks[i];
    const coversAcs = task.covers_acs ? task.covers_acs.join(',') : 'unknown';
    routeMapping += `// Task ${i + 1} (covers ${coversAcs}): ${targetPage || 'LLM_FILL: determine route'} → LLM_FILL: describe interaction\n`;
  }

  // Generate per-task functions
  let taskFunctions = '';
  for (let i = 0; i < tasks.length; i++) {
    const task = tasks[i];
    const taskNum = i + 1;

    taskFunctions += `
async function runTask${taskNum}(page, persona) {
  // Task: "${task.task || task}"
  // LLM_FILL: Navigate to the specific route for this task and perform
  // the interaction that distinguishes it from other tasks.
  // Each task function's final screenshot must show a visually distinct state.

  await navigateTo(page, '${targetPage}');
`;

    // LLM_FILL: add task-specific interactions (expand, hover, scroll, filter)
    // Available interactive elements from component-map:
    // ${componentMap?.interactive_elements?.expandable_rows?.length ? 'expandable_rows: expandRow(page, rowText)' : ''}
    // ${componentMap?.interactive_elements?.tooltips?.length ? 'tooltips: hoverElement(page, selector)' : ''}

    if (screenshots === 'key-only') {
      taskFunctions += `  await screenshot(page, \`persona-\${persona}-task-${taskNum}-final.png\`);
`;
    } else {
      taskFunctions += `  // LLM_FILL: add step-by-step screenshots as the persona navigates
  // await screenshot(page, \`persona-\${persona}-task-${taskNum}-step-1.png\`);
`;
    }

    taskFunctions += `}
`;
  }

  // Main loop
  const main = `
async function main() {
  const personas = ${JSON.stringify(personas)};
  const tasks = ${JSON.stringify(tasks.map((t, i) => ({ index: i + 1, task: t.task || t })))};

  const browser = await chromium.launch({ headless: true });

  for (const persona of personas) {
    for (let i = 0; i < tasks.length; i++) {
      const ctx = await browser.newContext({ viewport: { width: 1920, height: 900 } });
      const page = await ctx.newPage();
${addInitScriptBlock()}

      if (i === 0) await runTask1(page, persona);
${tasks.length >= 2 ? '      else if (i === 1) await runTask2(page, persona);' : ''}
${tasks.length >= 3 ? '      else if (i === 2) await runTask3(page, persona);' : ''}
${tasks.length > 3 ? tasks.slice(3).map((_, j) => `      else if (i === ${j + 3}) await runTask${j + 4}(page, persona);`).join('\n') : ''}
      await ctx.close();
    }
  }

  await browser.close();
  console.log(\`Completed walkthroughs for \${personas.length} personas x \${tasks.length} tasks\`);
}

main().catch(console.error);
`;

  return sharedHeader() + routeMapping + '\n' + pf6Utilities() + taskFunctions + main;
}

// ── Generate ──────────────────────────────────────────────────────────
const script = mode === 'verify' ? generateVerifyScript() : generateDiscoverScript();
writeFileSync(outputFile, script);
console.log(`Generated ${outputFile} (hash: ${cacheHash}, mode: ${mode})`);
